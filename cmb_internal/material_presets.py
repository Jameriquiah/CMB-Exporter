        stage.source_rgb1 = SOURCE_TO_NAME.get(words[7], "PRIMARY_COLOR")
        stage.source_rgb2 = SOURCE_TO_NAME.get(words[8], "PRIMARY_COLOR")
        stage.operand_rgb0 = OPERAND_RGB_TO_NAME.get(words[9], "SRC_COLOR")
        stage.operand_rgb1 = OPERAND_RGB_TO_NAME.get(words[10], "SRC_COLOR")
        stage.operand_rgb2 = OPERAND_RGB_TO_NAME.get(words[11], "SRC_COLOR")
        stage.source_alpha0 = SOURCE_TO_NAME.get(words[12], "PRIMARY_COLOR")
        stage.source_alpha1 = SOURCE_TO_NAME.get(words[13], "PRIMARY_COLOR")
        stage.source_alpha2 = SOURCE_TO_NAME.get(words[14], "PRIMARY_COLOR")
