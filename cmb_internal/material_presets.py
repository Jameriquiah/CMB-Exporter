@dataclass(frozen=True)
class MaterialPreset:
    label: str = ""
    fragment_lighting: bool = False
    vertex_lighting: bool = True
    is_fog_enabled: bool = True
    render_layer: int = 0
    face_culling: str = "BACK"
    polygon_offset_enabled: bool = False
    polygon_offset: int = 0
    emission_color: tuple[int, int, int, int] = (0, 0, 0, 0)
    ambient_color: tuple[int, int, int, int] = (102, 102, 102, 0)
