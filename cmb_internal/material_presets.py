def rgba8_to_float(color):
    return tuple(channel / 255.0 for channel in color)


def _used_texture_slots_from_stages(stages):
    used = set()
    for stage in stages:
        for word in stage:
            if word == 0x84C0:
                used.add(0)
            elif word == 0x84C1:
                used.add(1)
            elif word == 0x84C2:
                used.add(2)
    return used


def apply_preset_to_settings(settings, preset_name):
    preset = MATERIAL_PRESETS.get(preset_name)
    if preset is None:
        return
    settings.fragment_lighting = preset.fragment_lighting
    settings.vertex_lighting = preset.vertex_lighting
    settings.is_fog_enabled = preset.is_fog_enabled
    settings.render_layer = preset.render_layer
    settings.face_culling = preset.face_culling
    settings.polygon_offset_enabled = preset.polygon_offset_enabled
    settings.polygon_offset = preset.polygon_offset
    settings.emission_color = rgba8_to_float(preset.emission_color)
    settings.ambient_color = rgba8_to_float(preset.ambient_color)
    settings.diffuse_color = rgba8_to_float(preset.diffuse_color)
    settings.specular0_color = rgba8_to_float(preset.specular0_color)
    settings.specular1_color = rgba8_to_float(preset.specular1_color)
    for index, color in enumerate(preset.constant_colors):
        setattr(settings, f"constant{index}_color", rgba8_to_float(color))
    settings.buffer_color = preset.buffer_color
    settings.alpha_test_enabled = preset.alpha_test_enabled
    settings.alpha_reference = preset.alpha_reference
    settings.alpha_function = preset.alpha_function
    settings.depth_test_enabled = preset.depth_test_enabled
    settings.depth_write_enabled = preset.depth_write_enabled
    settings.depth_function = preset.depth_function
    settings.blend_mode = "ALPHA" if preset.blend_enabled else "OPAQUE"
    settings.blend_alpha_src_function = preset.blend_alpha_src_function
    settings.blend_alpha_dst_function = preset.blend_alpha_dst_function
    settings.blend_alpha_equation = preset.blend_alpha_equation
    settings.blend_color_src_function = preset.blend_color_src_function
    settings.blend_color_dst_function = preset.blend_color_dst_function
    settings.blend_color_equation = preset.blend_color_equation
    settings.blend_color = preset.blend_color
    used_texture_slots = _used_texture_slots_from_stages(preset.tex_env_stages)
    for index in TEXTURE_SLOT_INDICES:
        matrix_mode, reference_camera, source, scale, rotation, translation = preset.texture_coord_settings[index]
        if index not in used_texture_slots:
            matrix_mode, reference_camera, source = 0, 0, 0
            scale, rotation, translation = (1.0, 1.0), 0.0, (0.0, 0.0)
        setattr(settings, texture_slot_attr(index, "coord_matrix_mode"), matrix_mode)
        setattr(settings, texture_slot_attr(index, "coord_reference_camera"), reference_camera)
        image = getattr(settings, texture_slot_attr(index, "image"))
        mapping_attr = texture_slot_attr(index, "coord_mapping")
        mapping = getattr(settings, mapping_attr)
        if image is None:
            mapping = "NONE"
        elif mapping == "NONE":
            mapping = "UV"
        setattr(settings, mapping_attr, mapping)
        setattr(settings, texture_slot_attr(index, "coord_source"), source)
        setattr(settings, texture_slot_attr(index, "coord_scale"), scale)
        setattr(settings, texture_slot_attr(index, "coord_rotation"), rotation)
        setattr(settings, texture_slot_attr(index, "coord_translation"), translation)
    apply_stages_to_settings(settings, preset.tex_env_stages)


def ensure_stage_slots(settings):
    while len(settings.tex_env_stages) < 6:
        settings.tex_env_stages.add()


def apply_stages_to_settings(settings, stages):
    ensure_stage_slots(settings)
    settings.tex_env_stage_count = max(1, min(6, len(stages)))
    for stage_index, words in enumerate(stages[:6]):
        stage = settings.tex_env_stages[stage_index]
        stage.combine_rgb = COMBINE_TO_NAME.get(words[0], "REPLACE")
        stage.combine_alpha = COMBINE_TO_NAME.get(words[1], "REPLACE")
        stage.scale_rgb = str(words[2])
        stage.scale_alpha = str(words[3])
        stage.buffer_input_rgb = SOURCE_TO_NAME.get(words[4], "PREVIOUS_BUFFER_DMP")
        stage.buffer_input_alpha = SOURCE_TO_NAME.get(words[5], "PREVIOUS_BUFFER_DMP")
