def _preset_sort_key(item):
    key, preset = item
    if key.startswith("ADULT"):
        suffix = key[5:]
        if suffix.isdigit():
            return (0, int(suffix))
    if key.startswith("CHILD"):
        suffix = key[5:]
        if suffix.isdigit():
            return (1, int(suffix))
    return (2, preset.label)


